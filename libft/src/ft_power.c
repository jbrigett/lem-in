#include "libft.h"

double	ft_power(double x, long n)
{
	if (n == 0)
		return (1.0);
	if (n < 0)
		return (1.0 / (x * ft_power(1.0 / x, n + 1)));
	return (x * ft_power(x, n - 1));
}
